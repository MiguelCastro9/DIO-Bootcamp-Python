# commands for help within python itself

# within the terminal, execute the dir() command to display the context of methods that you can execute.
# inside the terminal run the command dir(100) to display the context of methods you can use for the entire type 
# represented by the number 100.

# within the terminal, execute the help() command to display help texts
# within the terminal execute the command help(100) to display help texts for the integer type represented by 
# number 100.

# to exit finish, execute the exit() command.