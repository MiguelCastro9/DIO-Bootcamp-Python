# tuples me python

# note: tuples are immutable, unlike lists which are mutable

fruits = ('orange', 'grape', 'mango',)
print(fruits)

letters = tuple('python')
print(letters)

numbers = tuple([1, 2, 3, 4, 5])
print(numbers)

matriz = (
    (1, 2, 3),
    (4, 5, 6),
    (7, 8, 9)
)
print(matriz[2][2])